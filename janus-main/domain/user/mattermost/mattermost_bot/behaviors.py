contradictory_pairs = [
    ("Introversion", "Extroversion"),
    ("Optimism", "Pessimism"),
    ("Honesty", "Dishonesty"),
    ("Kindness", "Selfishness"),
    ("Altruism", "Selfishness"),
    ("Empathy", "Selfishness"),
    ("Generosity", "Selfishness"),
    ("Kindness", "Aggression")
]

def is_valid_combination(combination):
    for a, b in contradictory_pairs:
        if a in combination and b in combination:
            return False
    return True

def calculate_joint_prob(role, combination):
    if not is_valid_combination(combination):
        return 0.0
    prob = 1.0
    behaviors = roles_behaviors[role]
    for behavior in combination:
        if behavior in behaviors:
            prob *= behaviors[behavior]
        else:
            return 0.0
    return prob

